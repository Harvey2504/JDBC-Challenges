package com.mindtree.CollegeApp.customException.service;

public class CollegeServiceException extends Exception {

	public CollegeServiceException() {

	}

	public CollegeServiceException(String arg0) {
		super(arg0);
	}

	public CollegeServiceException(Throwable arg0) {
		super(arg0);
	}

	public CollegeServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
